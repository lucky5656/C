#include<iostream>
using namespace std; 

int main()
{
	//可以用sizeof求出数据类型所占用内存大小
	//语法：sizeof(数据类型/变量)

	short  num1 = 10;

	cout << "short占用的空间为： " << sizeof(short) << endl;

	int num2 = 10;

	cout << "int占用的空间为： " << sizeof(int) << endl;

	long num3 = 10;

	cout << "long占用的空间为： " << sizeof(long) << endl;

	long long num4 = 10;

	cout << "long long占用的空间为： " << sizeof(long long) << endl;

	system("pause");

	return  0;

} 
