#include<iostream>
using namespace std;

int main()
{

	//比较运算符
	// 相等于：== 
	int a = 10;
	int b = 20;
	cout << (a == b) << endl;

	// 不等于：!=
	cout << (a != b) << endl;

	// 小于：<
	cout << (a < b) << endl;

	// 大于：>
	cout << (a > b) << endl;

	// 小于等于：<=
	cout << (a <= b) << endl;

	// 大于等于：>=
	cout << (a >= b) << endl;

	system("pause");

	return 0;

}